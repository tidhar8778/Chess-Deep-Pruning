def is_endgame(board):
    """

    :param board: chess board, type chess.Board
    :return: Either is a endgame or not.
    end game: if there are no queens on board
    """
    no_queens = False
    if len(board.pieces(chess.QUEEN, chess.WHITE)) == 0 and len(board.pieces(chess.QUEEN, chess.BLACK)) == 0:
        no_queens = True

    return no_queens

